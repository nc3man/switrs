cvc_keys = cvc_lookup.keys()

def lookup_cvc(cvc_code):
    # Usage:  cvc_desc = lookup_cvc(cvc_code)
    # Input:  cvc_code - string containing as substring the primary digits of a CVC section.
    # Output: cvc_desc - plaintext description of the CVC section.
    #                 Subsection references are ignored
    desc = ''
    for key in cvc_keys:
        if key in cvc_code:
            desc = cvc_lookup[key]
            break

    return desc

